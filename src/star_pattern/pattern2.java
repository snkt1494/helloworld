package star_pattern;

public class pattern2
{
     public static void main(String[] args)
     {
	//  *                              // reverse pattern create karaycha asel tr fakt star madhe vallu change karaychi aani star  -- kivva ++
    	                              // karaycha
    //	 * *
 //      * * *
      // * * * *
    	 
    	 
    	 int star =6;
    	 
    	 for(int i=1;i<=6;i++)   //rows
    	 {
    		 for(int j=1; j<=star;j++)  // clmn
    		 {	 
    		 System.out.print("*");
    	     }
    	 
    		 
    		 
    		 System.out.println(" ");
    		 star --;
    	 }
	}
}

